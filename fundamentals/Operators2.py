#logical operators
print (True or False)
print (7 != 3 and 2 > 3)

#and
print (True and True)
print (True and False)
print (False and True)
print (False and True)
print (True and True and False and True and True)

#or
print (True or True)
print (True or False)
print (False or True)
print (False or False)
print (False or False or True or False or False)

#XOR or !=
print (True != False)
print (False != False)

#not (unary)
print (not True)
print (not False)
print (not not -4)


